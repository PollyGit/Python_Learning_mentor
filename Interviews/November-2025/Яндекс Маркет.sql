- A не работает, B работает Р=1/3
- A работает, B не работает Р=1/3
- A не работает, B не работает Р=1/3

Те отвечаем на вопрос: Какова вероятность, что **кроме уже сломанного лифта, сломан и второй**?

Это последни вариант и Р=1/3



