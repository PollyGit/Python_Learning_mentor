--Задача 1
--10 раз подбрасывают монетку. Какова вероятность, что все 10 раз выпадет орел.
--Какова вероятность, что будет ровно 9 орлов?

У монетки вероятность орла = 1/2.

1. Чтобы все 10 раз выпал орёл, нужно:

P(10 орлов)=(1/2)^10

1. Вероятность, что ровно 9 орлов

    Тут важно, **в каком именно броске** выпала решка.

    Решка может быть в любой из 10 позиций → **10 разных способов**.

    P(ровно 9 орлов)=Сочетание(из 10 по 9) * (1/2)^10


